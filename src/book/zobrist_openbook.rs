#[inline]
fn piece_chess_index(ch: char) -> Option<usize> {
    match ch {
        'K' => Some(0),
        'A' => Some(1),
        'B' => Some(2),
        'N' => Some(3),
        'R' => Some(4),
        'C' => Some(5),
        'P' => Some(6),
        'k' => Some(7),
        'a' => Some(8),
        'b' => Some(9),
        'n' => Some(10),
        'r' => Some(11),
        'c' => Some(12),
        'p' => Some(13),
        _ => None,
    }
}

/// 与 `ZobristUtils.getZobristFromBoard` 一致；空位为 `.` 或空格。
pub fn zobrist_from_board(
    board: &[[char; 9]; 10],
    red_to_move: bool,
    left_right_swap: bool,
) -> Option<i64> {
    let mut z: i64 = 0;
    for (y, row) in board.iter().enumerate().take(10) {
        for (x, &cell) in row.iter().enumerate().take(9) {
            let mut ch = cell;
            if ch == '.' {
                ch = ' ';
            }
            if ch == ' ' {
                continue;
            }
            let chess = piece_chess_index(ch)?;
            let ix = if left_right_swap {
                (8usize - x) + y * 9
            } else {
                x + y * 9
            };
            let square = C90[ix] as usize;
            let idx = chess * 256 + square;
            z ^= ZOBRIST_TABLE[idx] as i64;
        }
    }
    if red_to_move {
        z ^= ZOBRIST_PLAYER;
    }
    Some(z)
}

/// 返回 (未左右镜像 Zobrist, 列镜像后 Zobrist)，与 Pf/Bh 开局库两次查询一致。
pub fn zobrist_pair_from_fen(fen: &str) -> Option<(i64, i64)> {
    let board = fen_to_board_matrix(fen)?;
    let red_to_move = fen_active_side_is_red(fen);
    let z1 = zobrist_from_board(&board, red_to_move, false)?;
    let z2 = zobrist_from_board(&board, red_to_move, true)?;
    Some((z1, z2))
}

#[inline]
fn san_for_c90_byte(b: u8) -> Option<&'static str> {
    C90.iter()
        .zip(SAN90.iter())
        .find(|(c, _)| **c == b)
        .map(|(_, s)| *s)
}

/// `vmove` 编码 → 引擎 UCI 四字符（如 `h2e2`），与 `ZobristUtils.getMoveFromVmove` 一致。
pub fn vmove_to_engine_uci(vmove: i32, left_right_swap: bool) -> Option<String> {
    let mut first = (vmove >> 8) & 0xFF;
    let mut second = vmove & 0xFF;
    if left_right_swap {
        first = (first & !15) | (14 - (first % 16));
        second = (second & !15) | (14 - (second % 16));
    }
    let a = san_for_c90_byte(first as u8)?;
    let b = san_for_c90_byte(second as u8)?;
    Some(format!("{a}{b}"))
}

fn fen_active_side_is_red(fen: &str) -> bool {
    let side = fen.split_whitespace().nth(1).unwrap_or("r");
    matches!(side, "w" | "W" | "r" | "R")
}

fn fen_to_board_matrix(fen: &str) -> Option<[[char; 9]; 10]> {
    let board_part = fen.split_whitespace().next()?;
    let mut board = [['.'; 9]; 10];
    let mut row = 0usize;
    let mut col = 0usize;

    for ch in board_part.chars() {
        match ch {
            '/' => {
                if col != 9 || row >= 9 {
                    return None;
                }
                row += 1;
                col = 0;
            }
            '1'..='9' => {
                let count = ch.to_digit(10)? as usize;
                if col + count > 9 {
                    return None;
                }
                for _ in 0..count {
                    board[row][col] = '.';
                    col += 1;
                }
            }
            piece if piece.is_ascii_alphabetic() => {
                if row >= 10 || col >= 9 {
                    return None;
                }
                board[row][col] = piece;
                col += 1;
            }
            _ => return None,
        }
    }

    if row != 9 || col != 9 {
        return None;
    }
    Some(board)
}
